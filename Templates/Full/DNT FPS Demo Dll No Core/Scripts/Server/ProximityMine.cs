        [Torque_Decorations.TorqueCallBack("", "ProximityMineData", "onThrow", "( %this, %user, %amount )", 3, 2300, false)]
        public string ProximityMineDataOnThrow(coItemData datablock, coPlayer player, int amount)
            {
            ShapeBaseShapeBaseDecInventory(player, datablock, 1);

            Torque_Class_Helper tch = new Torque_Class_Helper("ProximityMine", "");
            tch.Props.Add("datablock", datablock);
            tch.Props.Add("sourceObject", player);
            tch.Props.Add("rotation", string.Format("\"0 0 1 {0} \"", new Random().NextDouble()*360));
            tch.Props.Add("static", "false");
            tch.Props.Add("client", player["client"]);
            coProximityMine pm = (tch.Create());

            ((coSimSet) "MissionCleanup").pushToBack(pm);

            return pm;
            }

        [Torque_Decorations.TorqueCallBack("", "ProximityMineData", "onTriggered", "( %this, %obj, %target )", 3, 2300, false)]
        public void ProximityMineDataOnTriggered(coProximityMineData datablock, coProximityMine obj, string target)
            {
            }

        [Torque_Decorations.TorqueCallBack("", "ProximityMineData", "onExplode", "( %this, %obj, %position )", 3, 2300, false)]
        public void ProximityMineDataOnExplode(coProximityMineData datablock, coProximityMine shapebase, Point3F position)
            {
            // Damage objects within the mine's damage radius
            if (datablock["damageRadius"].AsFloat() > 0)
                RadiusDamage(shapebase, position, datablock["damageRadius"].AsFloat(), datablock["radiusDamage"].AsFloat(), datablock["damageType"], datablock["areaImpulse"].AsFloat());
            }

        [Torque_Decorations.TorqueCallBack("", "ProximityMineData", "damage", "( %this, %obj, %position, %source, %amount, %damageType )", 6, 2300, false)]
        public void ProximityMineDataDamage(coProximityMineData datablock, coProximityMine shapebase, TransformF position, coShapeBase source, string amount, string damageType)
            {
            // Explode if any damage is applied to the mine
            int r = 50 + (new Random().Next(0, 50));
            shapebase.schedule(r.AsString(), "explode");
            }

        [Torque_Decorations.TorqueCallBack("", "", "sendMsgClientKilled_MineDamage", "( %msgType, %client, %sourceClient, %damLoc )", 4, 2300, false)]
        public void SendMsgClientKilledMineDamage(string msgType, coGameConnection client, coShapeBase sourceClient, string damLoc)
            {
            // Customized kill message for deaths caused by proximity mines
            if (sourceClient == 0)
                MessageAll(msgType, "%1 was blown up!", client["playerName"]);
            else if (sourceClient == client)
                MessageAll(msgType, "%1 stepped on his own mine!", client["playerName"]);
            else
                MessageAll(msgType, "%1 was blown up by %2!", client["playerName"], sourceClient["playerName"]);
            }

