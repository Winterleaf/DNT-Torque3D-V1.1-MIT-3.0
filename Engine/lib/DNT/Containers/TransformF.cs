        public TransformF normalizeSafe()
            {
            double vmag = magnitudeSafe();

            if (vmag > POINT_EPSILON)
                {
                mPosition.x *= (float) (1.0/vmag);
                mPosition.y *= (float) (1.0/vmag);
                mPosition.z *= (float) (1.0/vmag);
                }
            return this;
            }

