        public EaseF(string easef)
            {
            string[] v = easef.Split(' ');
            _mDirection = v[0].AsInt();
            _mType = v[1].AsInt();
            try
                {
                _mParam1 = v[2].AsInt();
                _mParam2 = v[3].AsInt();
