            {
            if (((mPoint) != null) && (mExtent) != null)
                return string.Format("{0} {1} {2} {3} ", _mPoint.x.AsString(), _mPoint.y.AsString(), _mExtent.x.AsString(), _mExtent.y.AsString());
            else
                return "";
